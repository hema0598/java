package org.pratice;

public class Swapping {
	public static void main(String[] args) {
		int a=10;
		int b=10;
		System.out.println("before swapping");
		System.out.println("a is"+a);
		System.out.println("b is"+b);
		int c=a;
		a=b;
	    b=c;
	    System.out.println("before swapping");
	    System.out.println("a is"+a);
	    System.out.println("b is"+b);
	}

}
